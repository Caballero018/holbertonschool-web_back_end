#!/usr/bin/env python3
"""
Module containing an annotated function of type add that takes a float a
and a float b as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Type-annotated function add that takes a float a and a
    float b as arguments and returns their sum as a float.
    """
    return a + b
